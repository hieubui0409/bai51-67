Trong đoạn mã này:

Lớp Person:

Có hai thuộc tính FirstName và LastName.
Ghi đè phương thức Equals để so sánh hai đối tượng Person dựa trên FirstName và LastName.
Ghi đè phương thức GetHashCode để tạo mã băm duy nhất cho các đối tượng Person dựa trên FirstName và LastName.
Ghi đè phương thức ToString để trả về chuỗi đại diện của đối tượng Person.
Lớp Program:

Khởi tạo một Dictionary với khóa là Person và giá trị là string.
Thêm một số phần tử vào Dictionary.
Duyệt qua Dictionary và in ra các khóa và giá trị.
