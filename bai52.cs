Chúng ta khởi tạo một Dictionary với khóa là string và giá trị cũng là string.
Chúng ta thêm ba phần tử vào Dictionary với các khóa "Nam", "Lan", và "Mai" cùng các giá trị tương ứng "1A", "2B", và "3C".
Sử dụng vòng lặp foreach, chúng ta duyệt qua tất cả các phần tử trong Dictionary và in ra các khóa của chúng.
